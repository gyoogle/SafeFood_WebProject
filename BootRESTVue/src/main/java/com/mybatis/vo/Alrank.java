package com.mybatis.vo;

public class Alrank {
	private String al;
	private int cnt;
	
	public Alrank(String al, int cnt) {
		super();
		this.al = al;
		this.cnt = cnt;
	}

	public String getAl() {
		return al;
	}

	public void setAl(String al) {
		this.al = al;
	}

	public int getCnt() {
		return cnt;
	}

	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	
	
}
